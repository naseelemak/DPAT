    // Populates the cmbCards with account IDs from account.txt
    private void populateCards() {

        List<String> accounts;
        accounts = accMgmt.getEntries();
        cmbCards.removeAllItems();

        for (int i = 0; i < accounts.size(); i++) {
            cmbCards.addItem(accounts.get(i));
        }
    }

    // Populates the cmbFood with food items from food.txt
    private void populateFood() {

        List<String> food;
        food = foodMgmt.getNames();
        cmbFood.removeAllItems();
        cmbFood.addItem("--select--");

        for (int i = 0; i < food.size(); i++) {
            cmbFood.addItem(food.get(i));
        }
    }

    public void bindKiosks(Kiosk myK) {

        kiosk = myK;
        orderMgmt = new OrderManagement(this.kiosk);

    }

