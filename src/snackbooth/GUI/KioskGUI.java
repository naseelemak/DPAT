    public void updateTableTransactions() {
        List<String> transactionList;
        transactionList = orderMgmt.getEntries(kiosk.getKioskID());
        DefaultTableModel table = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        };
        tblTransactions.setAutoCreateRowSorter(true);
        tblTransactions.setModel(table);
        table.addColumn("Order ID");
        table.addColumn("Customer");
        table.addColumn("Item");
        table.addColumn("Cost (RM)");
        for (int i = 0; i < transactionList.size(); i = i + 4) {
            List<String> templist = new ArrayList<>();
            templist.add(transactionList.get(i));
            templist.add(transactionList.get(i + 1));
            templist.add(transactionList.get(i + 2));
            templist.add(transactionList.get(i + 3));
            table.addRow(templist.toArray());
        }
    }

