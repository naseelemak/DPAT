    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed

        int option;
        
        if (newBalance <= 0) {
            option = JOptionPane.showConfirmDialog(null, "Insufficient balance. Please top up your account and try again. Logging you out.", "Transaction Failed", JOptionPane.DEFAULT_OPTION);
        } else {
            
            option = JOptionPane.showConfirmDialog(null, "New balance: RM" + newBalance + "0\nPlease collect your item at the snack booth on Level 3.", "Payment successful!", JOptionPane.DEFAULT_OPTION);

            accMgmt.updateBalance(account, newBalance);

            orderMgmt.updateOrderCount();
            int orderID = orderMgmt.getOrderCount();

            if (product == 1) {
                drinkItem = size + drinkItem;

                orderMgmt.addOrder(orderID, account, drinkItem, cost);
            } else {

                foodItem = toasted + foodItem;
                orderMgmt.addOrder(orderID, account, foodItem, cost);

            }

            SnackBooth.getInstance().getMenu().updateTableMain();
            
        }

        if (option == JOptionPane.OK_OPTION) {
            System.out.println("OK!");
            kiosk.restart();
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

