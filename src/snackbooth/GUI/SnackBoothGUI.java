    // Writes drink modifications into the text file 
    public void writeTableDrinks() {
        List<String> newItems = drinkMgmt.getEntries();
        DefaultTableModel table = (DefaultTableModel) tblDrinks.getModel(); 

        int rows = table.getRowCount(), cols = table.getColumnCount();

        newItems.removeAll(newItems);

        for (int i = 0; i < rows; i++) {

            newItems.add(String.valueOf(table.getValueAt(i, 0)));
            newItems.add(String.valueOf(table.getValueAt(i, 1)));
            newItems.add(String.valueOf(table.getValueAt(i, 2)));
            newItems.add(String.valueOf(table.getValueAt(i, 3)));
            newItems.add(String.valueOf(table.getValueAt(i, 4)));
        }

        drinkMgmt.updateDrinks(newItems);
    }

    public void writeTableFood() {
        List<String> newItems = foodMgmt.getEntries();
        DefaultTableModel table = (DefaultTableModel) tblFood.getModel();

        int rows = table.getRowCount(), cols = table.getColumnCount();

        newItems.removeAll(newItems);

        for (int i = 0; i < rows; i++) {

            newItems.add(String.valueOf(table.getValueAt(i, 0)));
            newItems.add(String.valueOf(table.getValueAt(i, 1)));
            newItems.add(String.valueOf(table.getValueAt(i, 2)));
        }

        foodMgmt.updateFood(newItems);
    }

