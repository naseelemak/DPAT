    public void updateTableMain() {
        List<String> listeditem;
        listeditem = orderMgmt.getEntries();
        DefaultTableModel table = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        };
        tblMain.setAutoCreateRowSorter(true);
        tblMain.setModel(table);
        table.addColumn("Order ID");
        table.addColumn("Customer");
        table.addColumn("Item");
        table.addColumn("Cost (RM)");
        for (int i = 0; i < listeditem.size(); i = i + 4) {
            List<String> templist = new ArrayList<>();
            templist.add(listeditem.get(i));
            templist.add(listeditem.get(i + 1));
            templist.add(listeditem.get(i + 2));
            templist.add(listeditem.get(i + 3));
            table.addRow(templist.toArray());
        }
    }

